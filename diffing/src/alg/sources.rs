#![allow(non_snake_case)]
#![allow(non_camel_case_types)]
#![allow(unused)]
#![allow(dead_code)]

use std::fmt::{Debug, Display};
use std::{
    collections::HashSet,
    env, fs,
    iter::Iterator,
    ops::Deref,
    path::{Path, PathBuf},
};

/// pub struct Sources
/// Purpose: This struct will maintain/arrange/organize the
///          files being diffed -- by any of the algorithms.
///
pub struct Sources {
    dir: PathBuf,
    prevFiles: Vec<PathBuf>,
    currFiles: Vec<PathBuf>,
    currView: PathBuf,
}

impl Sources {
    pub fn new() -> Self {
        Self {
            dir: PathBuf::new(),
            prevFiles: Vec::<PathBuf>::new(),
            currFiles: Vec::<PathBuf>::new(),
            currView: PathBuf::new(),
        }
    }

    pub fn setDir(&mut self, d: PathBuf) -> bool {
        match d.is_dir() {
            false => false,
            true => {
                self.dir = d;
                true
            }
        }
    }

    pub fn init(&mut self) -> bool {
        match self.dir.is_dir() {
            false => false,
            true => true,
        }
    }
}

