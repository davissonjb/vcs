#![allow(non_snake_case)]
#![allow(non_camel_case_types)]
#![allow(unused)]
#![allow(dead_code)]

use io::*;

fn main() {
    let mut c: io::seek::Crawler = io::seek::Crawler::new();
    c.setCurr();
    c.seek();
    println!("Printing Crawler from fn main()!");
    c.print();
    println!("Hello, world!");
}

